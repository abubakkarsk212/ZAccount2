package zacc.popo.ltd.zaccount;

public class NewOObj {
    private String topics,availS,availSd,pS,pSd,thingsS,thingsSd,tc,tcd,val,valD,urlB;

    public NewOObj() {
    }

    public NewOObj(String topics, String availS, String availSd, String pS, String pSd, String thingsS, String thingsSd, String tc, String tcd, String val, String valD, String urlB) {
        this.topics = topics;
        this.availS = availS;
        this.availSd = availSd;
        this.pS = pS;
        this.pSd = pSd;
        this.thingsS = thingsS;
        this.thingsSd = thingsSd;
        this.tc = tc;
        this.tcd = tcd;
        this.val = val;
        this.valD = valD;
        this.urlB = urlB;
    }

    public String getAvailS() {
        return availS;
    }

    public String getAvailSd() {
        return availSd;
    }

    public String getpS() {
        return pS;
    }

    public String getpSd() {
        return pSd;
    }

    public String getTc() {
        return tc;
    }

    public String getTcd() {
        return tcd;
    }

    public String getThingsS() {
        return thingsS;
    }

    public String getThingsSd() {
        return thingsSd;
    }

    public String getTopics() {
        return topics;
    }

    public String getVal() {
        return val;
    }

    public String getValD() {
        return valD;
    }

    public String getUrlB() {
        return urlB;
    }
}
