package zacc.popo.ltd.zaccount;

public class OfferButtonColorObj {
   private String color;
   private String name;

    public OfferButtonColorObj() {
    }

    public OfferButtonColorObj(String color, String name) {
        this.color = color;
        this.name = name;
    }

    public String getColor() {
        return color;
    }

    public String getName() {
        return name;
    }
}
